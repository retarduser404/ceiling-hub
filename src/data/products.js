export const products = rawProducts.map(normalizeProduct);

export const getProductsByCategory = (categorySlug) => {
  return products.filter((p) => p.categorySlug === categorySlug);
};

export const getProductBySlug = (productSlug) => {
  return products.find((p) => p.slug === productSlug);
};

export const getProductById = (id) => {
  return products.find((p) => p.id === id);
};

export const getRelatedProducts = (productSlug, limit = 4) => {
  const product = getProductBySlug(productSlug);
  if (!product) return [];
  return products
    .filter((p) => p.categorySlug === product.categorySlug && p.slug !== productSlug)
    .slice(0, limit);
};